# ============================================================================
# SSE (Server-Sent Events) 相關常數
# ============================================================================

# SSE 廣播超時時間（秒）
# 在廣播訊息到客戶端佇列時，若超過此時間則視為客戶端無響應
SSE_BROADCAST_TIMEOUT_SECONDS = 5.0

# SSE Keepalive 超時時間（秒）
# 在此時間內若無訊息則發送 keepalive 心跳以保持連線
SSE_KEEPALIVE_TIMEOUT_SECONDS = 10.0


# ============================================================================
# QR Code 相關常數
# ============================================================================

# QR Code 每個方塊的像素大小
# 數值越大，生成的 QR Code 圖片尺寸越大
QR_CODE_BOX_SIZE = 10

# QR Code 邊框寬度（最小建議值為 4）
# 符合 QR Code 規範，確保掃描器能正確識別
QR_CODE_BORDER = 4


# ============================================================================
# HTTP Client 相關常數
# ============================================================================

# LINE Login API 請求超時時間（秒）
# 包含 JWKS 取得、Token 交換等 API 呼叫
LINE_LOGIN_HTTP_TIMEOUT_SECONDS = 10
